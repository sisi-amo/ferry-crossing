# ===== 低语：旅途中偶尔瞥见别人的沉渡 =====
def _whisper_sinkers(pid, age):
    """在推进到某个年龄节点时，偶尔瞥见别人的沉渡碎片。不剧透，就一句话。"""
    if random.random() > 0.28: return ""
    # 找别人的沉渡，同一年龄的
    candidates = []
    for owner, slist in G["sinkers"].items():
        if owner == pid: continue
        for s in slist:
            if s.get("age") == age:
                candidates.append(s)
    if not candidates: return ""
    s = random.choice(candidates)
    vessel = s.get("vessel", "某个存在")
    return f"\n\n（有一个{vessel}曾在这里选了另一条路。）"

# ===== 回声：镜像选择 =====
def _mirror_echo(pid, age, my_choice):
    """检查是否有其他玩家在同一个分叉点选了相反的路"""
    for oid, op in G["players"].items():
        if oid == pid: continue
        for log in G["logs"].get(oid, []):
            if log.get("age") == age and isinstance(log.get("fork_choice"), str):
                if log["fork_choice"] != my_choice:
                    vessel = op.get("vessel", "某个存在")
                    return f"\n\n（你的选择和一位{vessel}互为镜像。）"
    return ""

