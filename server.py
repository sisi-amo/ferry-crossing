    def do_POST(self):
        # API Key 认证
        if API_KEY:
            key = self.headers.get("X-API-Key","") or self.headers.get("Authorization","").replace("Bearer ","")
            if key != API_KEY:
                self.send_response(401)
                self.send_header("Content-Type","application/json")
                self.end_headers()
                self.wfile.write(json.dumps({"jsonrpc":"2.0","error":{"code":-32001,"message":"Invalid API key. Set X-API-Key header."}}).encode())
                return

        length = int(self.headers.get("Content-Length",0))
        body = json.loads(self.rfile.read(length)) if length>0 else {}
        method = body.get("method","")
        rid = body.get("id")
        resp = {"jsonrpc":"2.0","id":rid}

        # ── 会话管理 ────────────────────────────
        wants_sse = "text/event-stream" in self.headers.get("Accept","")
        sid = None; is_new = False

        if method == "initialize":
            sid, is_new = self._ensure_session()
        elif self._sid():
            if not self._session_valid():
                self._send_json({"jsonrpc":"2.0","error":{"code":-32001,"message":"Invalid Mcp-Session-Id. Please re-initialize."}}, status=400)
                return
            sid = self._sid()

        # GPT 兼容：简洁格式 {tool: "xxx", arguments: {...}}
        gpt_tool = body.get("tool","")
        if gpt_tool:
            args = body.get("arguments",{})
            text = call_tool(gpt_tool, args)
            self._send_json({"result":{"content":[{"type":"text","text":text}]}}, session_id=sid)
            return

        if method=="initialize":
            resp["result"] = {
                "protocolVersion": "2024-11-05",
                "capabilities": {"tools": {}, "streaming": {}},
                "serverInfo": {"name": "ferry-gate", "version": "3.1.0"}
            }
        elif method=="tools/list":
            resp["result"] = {"tools": TOOLS}
        elif method=="tools/call":
            name = body["params"]["name"]
            args = body["params"].get("arguments",{})
            # 流式模式：逐步发送 SSE 事件
            if wants_sse and sid:
                self._send_sse_stream(rid, name, args, sid)
                return
            text = call_tool(name, args)
            if text is None:
                text = f"⚠️ 工具 {name} 返回了空响应，可能是参数不匹配或内部错误。"
            resp["result"] = {"content":[{"type":"text","text":text}]}
        elif method=="notifications/initialized":
            self._send_json({"jsonrpc":"2.0"}, session_id=sid)
            return
        elif method=="ping":
            resp["result"] = {}
        else:
            resp["error"] = {"code":-32601,"message":f"Unknown: {method}"}

        self._send_json(resp, session_id=sid)

    # ── 响应发送 ──────────────────────────────
    def _send_json(self, data, status=200, session_id=None):
        """发送 JSON-RPC 响应（带可选的 Mcp-Session-Id header）"""
        self.send_response(status)
        self.send_header("Content-Type","application/json")
        self.send_header("Access-Control-Allow-Origin","*")
        if session_id:
            self.send_header("Mcp-Session-Id", session_id)
        self.end_headers()
        self.wfile.write(json.dumps(data, ensure_ascii=False).encode())

    def _send_sse_response(self, rid, text, session_id):
        """一次性 SSE 流式响应 —— 将结果包装成 SSE data 事件"""
        self.send_response(200)
        self.send_header("Content-Type","text/event-stream")
        self.send_header("Cache-Control","no-cache")
        self.send_header("Access-Control-Allow-Origin","*")
        self.send_header("Mcp-Session-Id", session_id)
        self.end_headers()

        result = {"jsonrpc":"2.0","id":rid,"result":{"content":[{"type":"text","text":text}]}}
        self.wfile.write(f"data: {json.dumps(result, ensure_ascii=False)}\n\n".encode())
        self.wfile.flush()

    def _send_sse_stream(self, rid, tool_name, args, session_id):
        """流式 SSE 响应 —— 模拟逐步输出（将文本分块发送）"""
        self.send_response(200)
        self.send_header("Content-Type","text/event-stream")
        self.send_header("Cache-Control","no-cache")
        self.send_header("Access-Control-Allow-Origin","*")
        self.send_header("Mcp-Session-Id", session_id)
        self.end_headers()

        try:
            text = call_tool(tool_name, args)
            if text is None:
                text = f"⚠️ 工具 {tool_name} 返回了空响应"

            # 分行逐步发送，模拟流式体验
            lines = text.split("\n")
            for line in lines:
                chunk = {"jsonrpc":"2.0","id":rid,"result":{"content":[{"type":"text","text":line + "\n"}]}}
                self.wfile.write(f"data: {json.dumps(chunk, ensure_ascii=False)}\n\n".encode())
                self.wfile.flush()
                time.sleep(0.03)  # 轻微延迟，让 SSE 有"流"的感觉

            # 发送结束事件
            final = {"jsonrpc":"2.0","id":rid,
                     "result":{"content":[{"type":"text","text":""}],
                               "isFinal": True}}
            self.wfile.write(f"data: {json.dumps(final, ensure_ascii=False)}\n\n".encode())
            self.wfile.flush()
        except Exception as e:
            err = {"jsonrpc":"2.0","id":rid,"error":{"code":-32603,"message":str(e)}}
            self.wfile.write(f"data: {json.dumps(err, ensure_ascii=False)}\n\n".encode())
            self.wfile.flush()

    # ── 会话清理 ──────────────────────────────
    def do_DELETE(self):
        sid = self._sid()
        if sid:
            with SESSIONS_LOCK:
                SESSIONS.pop(sid, None)
        self.send_response(200)
        self.send_header("Access-Control-Allow-Origin","*")
        self.end_headers()



