    if vg != ["无"]: lines.append(f"性别：{p.get('gender','?')}")
    lines += [
        f"环境：{p.get('parents','?')}",
        f"年龄：{age}",
        f"进度：{min(p['ageIdx'],len(mom))}/{len(mom)}节点",
        f"星图：{len(G['mapUnlocks'].get(pid,[]))}个地点已解锁",
    ]
    if is_echo:
        lines.append(f"残响：{len(my_sinkers)}个。每一步都沉在水底——logs是空的，但水底很满。")
