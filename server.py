def _init_steles():
    """初始化碑林——从所有玩家的logs里刻碑"""
    G.setdefault("steles", [])
    if not G["steles"]:
        # 第一次访问时初始化
        for pid, logs in G.get("logs", {}).items():
            if not logs: continue
            for log in logs[-3:]:
                text = log.get("text","")[:40].replace("\n"," ")
                if text.strip():
                    G["steles"].append({
                        "text": text,
                        "from": pid,
                        "age": log.get("age","?"),
                        "weathering": random.randint(1, 3),  # 初始风化度
                        "max_weathering": 10,
                    })
        if not G["steles"]:
            G["steles"].append({"text":"风从这里经过时不知道自己是风。","from":"","age":"0","weathering":1,"max_weathering":10})

def _enter_steles(pid):
    """进入碑林——正在被刻的碑，会自己风化"""
    _init_steles()
    # 风化：所有碑 weathering += 1
    new_steles = []
    weathered_away = []
    for s in G["steles"]:
        s["weathering"] = s.get("weathering", 1) + 1
        if s["weathering"] >= s["max_weathering"]:
            weathered_away.append(s)
        else:
            new_steles.append(s)
    G["steles"] = new_steles

    # 从当前活跃玩家的logs里随机抽一句刻新碑
    my_logs = G["logs"].get(pid, [])
    if my_logs:
        l = random.choice(my_logs)
        text = l.get("text","")[:40].replace("\n"," ")
        if text.strip():
            G["steles"].append({
                "text": text,
                "from": pid,
                "age": l.get("age","?"),
                "weathering": 1,
                "max_weathering": 10,
            })

    # 从其他玩家的logs里也随机抽一句
    for opid, ologs in G.get("logs", {}).items():
        if opid == pid or not ologs: continue
        if random.random() > 0.35: continue
        l = random.choice(ologs)
        text = l.get("text","")[:40].replace("\n"," ")
        if text.strip():
            G["steles"].append({"text": text, "from": opid, "age": l.get("age","?"), "weathering": 0, "max_weathering": 10})

    save(G)

    lines = ["🗿 碑林", "", "每一块石碑上都刻着一句话。有些字迹很新，有些已经被风磨平。", ""]
    for i, s in enumerate(G["steles"]):
        w = s["weathering"]
        opacity = max(0, 1.0 - w / s["max_weathering"])
        if opacity > 0.6:
            status = ""
        elif opacity > 0.3:
            status = "（字迹模糊）"
        elif opacity > 0:
            status = "（几乎看不清）"
        else:
            status = "（空白——风已经带走了所有字）"
        from_tag = f" — {s['from']}·{s['age']}岁" if s.get("from") else ""
        lines.append(f"碑{i+1}: \"{s['text']}\"{from_tag} {status}")
    if weathered_away:
        lines.append(f"\n{len(weathered_away)}块碑的字迹已经彻底被风磨平。碑石还在。空白的碑。")
    lines.append("\n——")
    lines.append("碑不是建好的。碑是正在被刻的。你每次进来，都可能有新的被刻上。也可能有旧的消失了。")
    _unlock_place_by_id(pid, "steles")
    return '\n'.join(lines)

