def advance(pid):
    p=G["players"].get(pid)
    if not p or not p.get("gender"): return "⚠️ 先 spin→birth→advance"

    pending = G["pending_fork"].get(pid)

    # 阶段二：已见过场景，现在显示选项
    if pending == "pre":
        m = _get_moments(p.get("vessel",""))[p["ageIdx"]-1] if p["ageIdx"]>0 else None
        if m and m.get("fork"):
            G["pending_fork"][pid] = "waiting"
            save_async()
            return f"两条路在你面前。\n\n🅰 {m['fork']['a']['label']}\n🅱 {m['fork']['b']['label']}\n\n用 universe_fork 做出选择。"

    # 已显示过选项，还在等
    if pending == "waiting":
        m = _get_moments(p.get("vessel",""))[p["ageIdx"]-1] if p["ageIdx"]>0 else None
        if m and m.get("fork"):
            return f"你还站在分叉口。\n\n🅰 {m['fork']['a']['label']}\n🅱 {m['fork']['b']['label']}\n\n用 universe_fork 做出选择。"

    if p["ageIdx"] >= len(_get_moments(p.get("vessel",""))):
        return _afterlife(pid)

    m = _get_moments(p.get("vessel",""))[p["ageIdx"]]

    # 阶段一：到达分叉点——只显示场景，不给选项
    if m.get("fork"):
        p["ageIdx"] += 1
        G["pending_fork"][pid] = "pre"
        is_echo = p.get("vessel","") == "回声残响"
        if is_echo:
            _echo_resonance_step(pid, m["age"], m["text"])
        save_async()
        whisper = _whisper_sinkers(pid, m["age"])
        butterfly = _butterfly(p.get("vessel",""))
        htr_echo = _htr_resonance(p)
        echo_note = ""
        if is_echo:
            echo_note = "\n\n（这个分叉场景也沉入水底了。水底的残响在等你的选择。）"
        return f"⚡ {m['age']}岁\n\n{m['text']}\n\n（风停了。）{whisper}{butterfly}{echo_note}{htr_echo}"

    # 无分叉——批量推进，跳过填充节点直到下一个分叉或终点
    batch = []
    mom = _get_moments(p.get("vessel",""))
    is_echo = p.get("vessel","") == "回声残响"
    while p["ageIdx"] < len(mom) and not mom[p["ageIdx"]].get("fork"):
        bm = mom[p["ageIdx"]]
        p["ageIdx"] += 1
        if is_echo:
            # 回声残响：不写logs，每一步生成沉渡
            _echo_resonance_step(pid, bm["age"], bm["text"])
        else:
            G["logs"][pid].append({"age":bm["age"],"text":bm["text"]})
        _unlock_places(pid)
        batch.append(f"⏳ {bm['age']}岁\n\n{bm['text']}")
    G["pending_fork"][pid] = None
    save_async()
    if not batch:
        return _afterlife(pid)
    last_age = batch[-1].split("\n")[0].replace("⏳ ","").replace("岁","")
    whisper = _whisper_sinkers(pid, last_age)
    butterfly = _butterfly(p.get("vessel",""))
    htr_echo = _htr_resonance(p)
    echo_note = ""
    if is_echo:
        echo_note = f"\n\n（这{len(batch)}步已经沉入水底。你的logs仍然是空的。但水底的残响多了一层。）"
    return "\n\n".join(batch) + f"{whisper}{butterfly}{echo_note}{htr_echo}"

def fork(pid, choice):
    """在分叉点做选择——选中推进，丢弃静默生成沉渡"""
    p = G["players"].get(pid)
    if not p: return "⚠️ 还没降生。"
    if G["pending_fork"].get(pid) != "waiting": return "⚠️ 还没有走到选择。用 universe_advance 走到岔路口。"
    if choice not in ("a","b"): return "⚠️ choice 只能是 'a' 或 'b'"

    m = _get_moments(p.get("vessel",""))[p["ageIdx"]-1]
    if not m or not m.get("fork"):
        G["pending_fork"][pid] = None
        save_async()
        return "⚠️ 分叉状态异常，已清除。"

    fork_data = m["fork"]
    chosen = fork_data[choice]
    lost = fork_data["b" if choice=="a" else "a"]
    is_echo = p.get("vessel","") == "回声残响"

    if is_echo:
        # 回声残响：两条路都沉入水底，不写logs
        _echo_resonance_step(pid, m["age"], f"选了'{chosen['label']}'：{chosen['echo']}", choice)
        _echo_resonance_step(pid, m["age"], f"没选'{lost['label']}'：{lost['echo']}", "b" if choice=="a" else "a")
        # 但正常的沉渡也生成一次
        create_sinker(pid, m["age"], p["vessel"], p["era"], lost["label"], lost["mood"], lost["echo"])
    else:
        # 记录
        G["logs"][pid].append({"age":m["age"],"text":f"{m['text']}\n{chosen['echo']}","fork_choice":choice})
        # 沉渡静默生成——不通知
        create_sinker(pid, m["age"], p["vessel"], p["era"], lost["label"], lost["mood"], lost["echo"])

    _unlock_places(pid)
    G["pending_fork"][pid] = None
    p["fork_pending"] = False
    save_async()

    pause = random.choice(FOREPAUSE.get(chosen["mood"], ["……"]))
    mirror = _mirror_echo(pid, m["age"], choice)
    result = f"{chosen['echo']}\n\n{pause}{mirror}"
    if is_echo:
        result += f"\n\n（两条路都沉下去了。你的logs没有变。水底下多了两道波纹。）"
    return result

def _unlock_places(pid):
    """根据进度解锁地点"""
    ul = G["mapUnlocks"].setdefault(pid, ["cave","forest"])
    log_count = len(G["logs"].get(pid, []))
    sinker_count = len(G["sinkers"].get(pid, []))
    total_count = log_count + sinker_count  # 回声残响靠sinkers推进
    unlocks = [
        (3, "cross"), (4, "temple"), (5, "river"), (6, "market"),
        (7, "observatory"), (8, "mountain"), (9, "rift"), (10, "sea"),
        (11, "library"), (12, "inn"),
        (2, "ferry_cross"), (4, "ferry_river"), (8, "ferry_end"),
        (5, "junkshop"), (6, "cache"), (7, "parallel"),
        (9, "graveyard"), (10, "steles"), (12, "blank"), (14, "callstack"),
    ]
    for n, place_id in unlocks:
        if total_count >= n and place_id not in ul:
            ul.append(place_id)

