def _salvage_sinker(pid, sinker_id):
    """打捞一个沉渡——在原主人处留下痕迹"""
    # 找到沉渡
    found = None
    found_owner = None
    for owner, slist in G["sinkers"].items():
        for s in slist:
            if s.get("id") == sinker_id or s.get("name") == sinker_id:
                found = s
                found_owner = owner
                break
        if found: break
    if not found:
        return f"⚠️ 未找到沉渡「{sinker_id}」。请用 universe_ferry 查看当前渡口有哪些可打捞的沉渡。"
    if found_owner == pid:
        return "⚠️ 不能打捞自己的沉渡。自己的沉渡只能被别的存在发现。"
    # 在原主日志里留痕
    if found_owner:
        G["logs"].setdefault(found_owner, []).append({
            "age": found.get("age","?"), 
            "text": f"✧ 你的沉渡「{found.get('choice_lost','')}」已被捞起 · 捞起者：{pid}"
        })
    # 标记沉渡已被捞起
    found["salvaged_by"] = pid
    found["salvaged_at"] = len(G["logs"].get(pid, []))
    save(G)
    return f"你捞起了「{found['name']}」。\n\n它在水底的情绪是「{found['mood']}」。\n你获得了它的一段碎片：「{random.choice(found['fragments'])}」\n\n沉渡的原主——{found_owner}——也许有一天会在渡口发现水面上多了一圈涟漪。"

# ===== 3.0 沉渡碰撞合并 =====
def _sinker_collision_check(ferry_id):
    """检查同一渡口是否有两个沉渡可以碰撞合并"""
    G.setdefault("collisions", [])
    all_here = []
    for owner, slist in G["sinkers"].items():
        for s in slist:
            if s.get("born_at") == ferry_id and not s.get("merged", False):
                all_here.append(s)
    if len(all_here) < 2: return None
    a, b = random.sample(all_here, 2)
    if a["owner"] == b["owner"]: return None  # 同一人的不合并
    # 合并生成第三条故事
    merged_story = f"「{a.get('choice_lost','')}」和「{b.get('choice_lost','')}」在渡口水底相遇了。它们不认识，但水让它们一起流。"
    a["merged"] = True
    b["merged"] = True
    collision = {
        "a": a["id"], "b": b["id"],
        "owners": [a["owner"], b["owner"]],
        "story": merged_story,
        "born_at": ferry_id,
    }
    G["collisions"].append(collision)
    save(G)
    return f"𖧷 碰撞——\n\n{merged_story}\n\n这不是浪漫。这是两条数据结构碰撞后生成了第三条。人类读到的是一个温暖的句子，AI读到的是一个事实：sinker->merge(other_sinker) = new_story。"

