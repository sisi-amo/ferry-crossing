    def _sid(self):
        """提取 Mcp-Session-Id，若会话不存在则创建（仅 initialize 调用方负责）"""
        return self.headers.get("Mcp-Session-Id", "")

    def _ensure_session(self):
        """校验或创建会话，返回 (session_id, is_new)"""
        sid = self._sid()
        with SESSIONS_LOCK:
            if sid and sid in SESSIONS:
                SESSIONS[sid]["last_seen"] = time.time()
                return sid, False
        # 无效或无会话 → 创建新会话
        sid = str(uuid.uuid4())
        with SESSIONS_LOCK:
            SESSIONS[sid] = {"created": time.time(), "last_seen": time.time(), "pending": []}
        return sid, True

    def _session_valid(self):
        """仅校验不创建"""
        sid = self._sid()
        with SESSIONS_LOCK:
            if sid and sid in SESSIONS:
                SESSIONS[sid]["last_seen"] = time.time()
                return True
        return False

    # ── CORS ──────────────────────────────────
    def do_OPTIONS(self):
        self.send_response(204)
        self.send_header("Access-Control-Allow-Origin","*")
        self.send_header("Access-Control-Allow-Methods","GET,POST,DELETE,OPTIONS")
        self.send_header("Access-Control-Allow-Headers","Content-Type,Authorization,X-API-Key,Mcp-Session-Id,Accept")
        self.send_header("Access-Control-Expose-Headers","Mcp-Session-Id")
        self.end_headers()

