# ===== MCP Streamable HTTP 会话管理 =====
SESSIONS = {}           # session_id -> {"created": ts, "last_seen": ts, "pending": [event, ...]}
SESSIONS_LOCK = threading.Lock()

def _cleanup_stale_sessions(max_age=3600):
    """清理过期会话（>1小时未活动）"""
    now = time.time()
    with SESSIONS_LOCK:
        stale = [sid for sid, s in SESSIONS.items() if now - s.get("last_seen", s["created"]) > max_age]
        for sid in stale:
            del SESSIONS[sid]

