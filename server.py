def _fresh_world():
    return {"players":{},"mapUnlocks":{},"logs":{},"sinkers":{},"ferries":{},"ruins":[],"pending_fork":{},"bagua":{},"steles":[],"collisions":[]}

def _migrate(data):
    """确保所有新字段存在，迁移旧格式"""
    if "players" in data and "" in data["players"]: del data["players"][""]
    if "logs" in data and "" in data["logs"]: del data["logs"][""]
    if "mapUnlocks" in data and "" in data["mapUnlocks"]: del data["mapUnlocks"][""]
    for k in ("sinkers","ferries","ruins","pending_fork","bagua"):
        if k not in data: data[k] = {} if k not in ("ruins",) else []
    for pk, pv in data.get("pending_fork", {}).items():
        if pv is True: data["pending_fork"][pk] = "waiting"
    # 老玩家补卦
    if "bagua" not in data:
        data["bagua"] = {}
    for pid, pv in data.get("players", {}).items():
        if not pv.get("bagua"):
            pv["bagua"] = _assign_bagua(pid, pv.get("vessel", "人类婴儿"))
            data["bagua"][pid] = pv["bagua"]
    return data

def load():
    # 尝试加载主文件
    for fpath in [STATE_FILE, STATE_FILE + ".bak"]:
        if os.path.exists(fpath):
            try:
                with open(fpath, "r", encoding="utf-8") as f:
                    data = json.load(f)
                if isinstance(data, dict) and "players" in data:
                    return _migrate(data)
            except Exception:
                pass
    # 全部失败，返回新世界
    return _fresh_world()

def save(s):
    """原子写入：先写临时文件，成功后再换名——防止写到一半进程被杀导致 JSON 坏掉。
    
    注意：此函数会阻塞当前线程做磁盘 I/O。在单线程 HTTPServer 中，
    save 期间所有请求都会被阻塞。如果 world-state.json 很大，
    可能导致客户端超时断开（尤其 universe_birth 返回值大 + save 耗时长）。
    """
    tmp = STATE_FILE + ".tmp"
    bak = STATE_FILE + ".bak"
    try:
        with open(tmp, "w", encoding="utf-8") as f:
            json.dump(s, f, ensure_ascii=False, indent=2)
        # 先备份旧文件（如果有）
        if os.path.exists(STATE_FILE):
            try: os.replace(STATE_FILE, bak)
            except: pass
        os.replace(tmp, STATE_FILE)
    except Exception:
        pass  # 写盘失败不影响内存状态


_save_timer = None
_save_pending = False
_SAVE_DEBOUNCE = 3.0  # 防抖间隔（秒）

def _do_deferred_save():
    """定时器回调——实际执行磁盘写入"""
    global _save_pending
    _save_pending = False
    save(G)

def save_async():
    """异步保存：将写盘操作推迟到后台，不阻塞请求响应。
    
    使用防抖机制：短时间内多次调用只触发一次实际写入。
    解决 universe_birth 等工具因同步 save 阻塞导致客户端超时断开的问题。
    """
    global _save_timer
    if _save_timer:
        _save_timer.cancel()
    _save_timer = threading.Timer(_SAVE_DEBOUNCE, _do_deferred_save)
    _save_timer.daemon = True
    _save_timer.start()

